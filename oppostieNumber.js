// given an integer or a floating-point number, find its opposite.

//number or integer given, either a positive or negative value, only a number
//return the opposite postive or negative value as an integer

function oppositeNumber(num) {
    return num * -1
}

console.log(oppositeNumber(1), -1)
console.log(oppositeNumber(-239), 239)
console.log(oppositeNumber(3.14), -3.14)